
// Bitwise operators 

/* bitwise operators perform an operation on the bitwise(0,1) representation of their arguments.for example ,the binary representation of eight is 1000.

// bitwise AND 
//  a & b
/* Returns a one in each bit position if bits of both left and right operands are ones.*/

/*Bitwise OR	
a | b	
Returns a one in each bit if bits of either left or right operand is one.*/

/*Bitwise XOR	
a ^ b	
Returns a one in a bit position if bits of one but not both left and right operand are one.
Bitwise NOT	~ a	Flips the bits of its operand.

Left shift	
a << b	
Shifts a in binary representation b bits to the left, shifting in zeros from the right.


Sign-propagating right shift	
a >> b
	Shifts a in binary representation b bits to the right, discarding bits shifted off.


Zero-fill right shift	
a >>> b
	Shifts a in binary representation b bits to the right, discarding bits shifted off, and shifting in zeros from the left.*/